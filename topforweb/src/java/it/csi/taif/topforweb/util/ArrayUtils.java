    /**
     *
     * @param array
     * @return
     */
    public static <T> boolean notContainsNulls(T[] array) {
        return ! containsNulls(array);
    }

    /**
     *
     * @param array
     * @return
     */
    public static <T> boolean containsNulls(T[] array) {
        @SuppressWarnings("unchecked")
        final boolean result = contains(array, (Class<T>) Object.class, new Predicate<T>() {

            public boolean apply(T object) {
                return object == null;
            }

        });

        return result;
    }

    /**
     *
     * @param array
     * @param arrayType
     * @param predicate
     * @return
     */
    public static <T> boolean notContains(T[] array, Class<T> arrayType, Predicate<T> predicate) {
        return ! contains(array, arrayType, predicate);
    }

    /**
     *
     * @param array
     * @param arrayType
     * @param predicate
     * @return
     */
    public static <T> boolean contains(T[] array, Class<T> arrayType, Predicate<T> predicate) {
        final T[] filtered = filter(array, arrayType, predicate);

        return org.apache.commons.lang3.ArrayUtils.isNotEmpty(filtered);
    }

    // === Inspecting === END ==================================================

    // === Filtering === BEGIN =================================================

    /**
     *
     * @param array
     * @return
     */
    public static <T> T[] filterNulls(T[] array) {
        @SuppressWarnings("unchecked")
        final T[] result = (T[]) filterNulls(array, Object.class);

        return result;
    }

    /**
     *
     * @param array
     * @param arrayType
     * @return
     */
    public static <T> T[] filterNulls(T[] array, Class<T> arrayType) {
        return filter(array, arrayType, new Predicate<T>() {

            public boolean apply(T object) {
                return object != null;
            }

        });
    }

    /**
     *
     * @param array
     * @param predicate
     * @return
     */
    public static <T> T[] filter(T[] array, Predicate<T> predicate) {
        @SuppressWarnings("unchecked")
        final T[] result = filter(array, (Class<T>) Object.class, predicate);

        return result;
    }

    /**
     *
     * @param array
     * @param arrayType
     * @param predicate
     * @return
     */
    public static <T> T[] filter(T[] array, Class<T> arrayType, Predicate<T> predicate) {
        final List<T> filteredList = new ArrayList<T>();
        for (final T object : array) {
            if (predicate.apply(object)) {
                filteredList.add(object);
            }
        }

        @SuppressWarnings("unchecked")
        final T[] runtimeArray = (T[]) Array.newInstance(arrayType, filteredList.size());

        final T[] filteredArray = filteredList.toArray(runtimeArray);

        return filteredArray;
    }

