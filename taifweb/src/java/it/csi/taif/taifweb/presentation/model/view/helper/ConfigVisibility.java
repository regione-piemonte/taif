package it.csi.taif.taifweb.presentation.model.view.helper;

import java.util.ArrayList;

public class ConfigVisibility {
	
	private ArrayList<String> clean;
	private ArrayList<String> activate;
	private ArrayList<String> deactivate;
	private ArrayList<String> on;
	private ArrayList<String> off;
	private ArrayList<String> hide;
	private ArrayList<String> show;
	public ArrayList<String> getClean() {
		return clean;
	}
	public void setClean(ArrayList<String> clean) {
		this.clean = clean;
	}
	public ArrayList<String> getActivate() {
		return activate;
	}
	public void setActivate(ArrayList<String> activate) {
		this.activate = activate;
	}
	public ArrayList<String> getDeactivate() {
		return deactivate;
	}
	public void setDeactivate(ArrayList<String> deactivate) {
		this.deactivate = deactivate;
	}
	public ArrayList<String> getOn() {
		return on;
	}
	public void setOn(ArrayList<String> on) {
		this.on = on;
	}
	public ArrayList<String> getOff() {
		return off;
	}
	public void setOff(ArrayList<String> off) {
		this.off = off;
	}
	public ArrayList<String> getHide() {
		return hide;
	}
	public void setHide(ArrayList<String> hide) {
		this.hide = hide;
	}
	public ArrayList<String> getShow() {
		return show;
	}
	public void setShow(ArrayList<String> show) {
		this.show = show;
	}
	
	
	
	
	
}
